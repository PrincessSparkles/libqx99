/* ************************************************************************* */

#include <cstdint>  // std::uint32_t

/* ************************************************************************* */

namespace
{
	const std::uint16_t VERSION_MAJOR = 0;
	const std::uint16_t VERSION_MINOR = 1;
}

/* ************************************************************************* */
/* ************************************************************************* */

std::uint32_t GetVersion()
{
	return VERSION_MAJOR << 16 | VERSION_MINOR;
}

