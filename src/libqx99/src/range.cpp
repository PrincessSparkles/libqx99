#include "qx99/range.h"  // qx99::range

/* ************************************************************************* */

using qx99::Range;

/* ************************************************************************* */
/* ************************************************************************* */

Range::Range(RangeDigitType value) :
    Range(value, value)
{
}

/* ************************************************************************* */

Range::Range(RangeDigitType low, RangeDigitType high) :
    _low(low),
    _high(high)
{
}

/* ************************************************************************* */

bool Range::Contains(RangeDigitType value) const
{
    bool result = false;
    
    if (_low <= value && _high >= value)
    {
        // result is initialised to false, so we don't need the else clause
        result = true;        
    }
    
    return result;
}

